def is_valid_sudoku(board)
  col_arr = []
  board.each_with_index {|a, i| col_arr << a[0]}
  col_arr = col_arr.map! {|c| c if c != "."}.compact 
  if col_arr.uniq == col_arr 
    return true 
  else 
    return false
  end
end

